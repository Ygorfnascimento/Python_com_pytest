def soma(a, b):
    return a + b

def subtracao(a, b):
    return a - b

def multiplicacao(a, b):
    return a * b

def divisao(a, b):
    return a / b

def resto_divisao(a, b):
    return a % b

def media(a, b, c):
    return (a + b + c) / 3

def area_retangulo(base, altura):
    return base * altura